#include "../includes/libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int n;
	int k;

	n = 0;
	k = 0;
	while (s1[n])
		n++;
	while (s2[k])
	{
		s1[k + n] = s2[k];
		k++;
	}
	s1[k + n] = '\0';
	return (s1);
}
